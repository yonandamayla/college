SELECT D.DEPARTEMEN, P.PEGAWAI, P.TGL_LAHIR 
FROM pegawai P 
INNER JOIN departemen D ON P.NO_DEP = D.NO_DEP 
WHERE P.TGL_LAHIR = (SELECT MAX(TGL_LAHIR) FROM pegawai);

-- no 2
SELECT D.DEPARTEMEN, COUNT(P.NO_KTP) as JUMLAH_PEGAWAI 
FROM departemen D 
LEFT JOIN pegawai P ON D.NO_DEP = P.NO_DEP 
GROUP BY D.DEPARTEMEN;

-- no 3
SELECT 
    P.NO_PRO, 
    P.PROJEK, 
    P.LOKASI,
    P.NO_DEP, 
    D.NO_DEP,
    D.DEPARTEMEN, 
    D.LOKASI 
FROM 
    proyek P 
INNER JOIN 
    departemen D ON P.NO_DEP = D.NO_DEP
ORDER BY 
    P.NO_PRO ASC;