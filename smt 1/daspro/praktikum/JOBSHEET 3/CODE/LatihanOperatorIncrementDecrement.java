public class LatihanOperatorIncrementDecrement {
    public static void main(String[] args) {
        int i = 1;
        // increment
        System.out.println("i : " + i);
        System.out.println("++i : " + ++i);
        System.out.println("i++ : " + i++);
        System.out.println("i : " + i);
        System.out.println(" ");
        // decrement
        System.out.println("--i : " + --i);
        System.out.println("i-- : " + i--);
        System.out.println("i : " + i );

    }
}